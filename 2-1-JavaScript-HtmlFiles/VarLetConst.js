// Global Scope: The var DataType has Global Scope.
// We can update the value of var.
// We can be Redeclared var.
var a = 10;

// The let and const keyword was introduced in ES6 (2015).
// Block Scope: The scope of let is within the Block only.
// We can update the value of let.
// We cannot be Redeclared let within same block.
let b = 20;

// Block Scope: The scope of const is within the Block only.
// We cannot update the value of const.
// We cannot be Redeclared const within same block.
const c = 30;
